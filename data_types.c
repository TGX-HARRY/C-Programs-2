#include <stdio.h>

int main() {
    int a=9; // +ve int
    int b=-9; // -ve int
    int c=89U; // U for Unsigned int
    int d=99998L; // L for long int
    printf("Positive integer: %d\n", a);
    printf("Negative Integer: %d\n", b);
    printf("Unsigned Integer: %u\n", c); // %u for unsigned int
    printf("Long Integer: %ld\n", d); // %ld for long int

    
}